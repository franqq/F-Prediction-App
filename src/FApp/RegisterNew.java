    private static void addUser(String uname,String pass)
    {
        try {
          Statement statement = c.createStatement();
               String s = "INSERT INTO `dailystatistics`.`users` (`user Name` ,`password`)  VALUES ('" + uname + "', '" + pass+"');";
               int executeUpdate = statement.executeUpdate(s);
               
        } catch (SQLException ex) {
            Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    protected static boolean confirmPass(String p, String conf)
    {
        boolean bool=false;
        
        if(p == null ? conf != null : !p.equals(conf))
        {
            bool=false;
        }
        else if(p.equals(conf))
        {
            bool = true;
        }
       return bool;
    }
    
    
    //check if user exists
    private static boolean checkPrimaryKey(String user)
    {
         ResultSet rst = null;
        
        try {
            
          Statement statement = c.createStatement();
          String s = "SELECT * FROM users WHERE `user Name` = '" + user + "';";
              

               rst = statement.executeQuery(s);
               
               
        } catch (SQLException ex) {
            Logger.getLogger(LoginScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
               if(rst == null)
                   return false;
               else
                   return true;
    }
    
    
